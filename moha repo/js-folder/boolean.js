//  A boolean data type can have only two values: true or false

// Using Booleans in Conditional Statements:

let isSunny = true;

if (isSunny) {
    console.log("It's sunny outside!");
} else {
    console.log("It's not sunny outside.");
}


//   Comparisons

let num1 = 10;
let num2 = 5;

let isGreater = num1 > num2;
console.log(isGreater); // Output: true


// Logical Operators

let hasMoney = true;
let isHungry = false;

let canBuyFood = hasMoney && isHungry;
console.log(canBuyFood); // Output: false

