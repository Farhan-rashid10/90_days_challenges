// Logical OR (||):Example 1: Providing multiple conditions for executing code:

let day = "Saturday";

if (day === "Saturday" || day === "Sunday") {
    console.log("It's a weekend!");
} else {
    console.log("It's a weekday.");
}


// Logical NOT (!):Example 1: Checking if a value is falsy:

let loggedIn = true;

if (!loggedIn) {
    console.log("Please log in to continue.");
} else {
    console.log("You are already logged in.");
}


